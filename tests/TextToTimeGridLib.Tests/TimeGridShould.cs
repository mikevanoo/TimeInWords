    [Theory]
    [ClassData(typeof(ToStringTheoryData))]
    public void BuildCorrectToStringValueGivenString(string input, bool strict, string expected)
    {
        var timeGrid = new TestTimeGrid();

        var bitMask = timeGrid.GetBitMask(input, strict);
        var actual = timeGrid.ToString(bitMask);

        testOutputHelper.WriteLine(actual);
        actual.Should().BeEquivalentTo(expected);
    }

#pragma warning disable xUnit1004
    [Fact(Skip = "test code generator")]
#pragma warning restore xUnit1004
    public void GenerateToStringTheoryData()
    {
        var timeGrid = new TestTimeGrid();
        var preset = new EnglishPreset();
        var result = new StringBuilder();

        var time = new DateTime(2024, 1, 1, 0, 0, 0);
        while (time.Hour < 12)
        {
            var timeAsText = preset.Format(time).TimeAsText;

            var bitMask = timeGrid.GetBitMask(timeAsText, true);
            result.AppendLine(
                CultureInfo.InvariantCulture,
                $"Add(\"{timeAsText}\", true, @\"{timeGrid.ToString(bitMask)}\");"
            );

            bitMask = timeGrid.GetBitMask(timeAsText, false);
            result.AppendLine(
                CultureInfo.InvariantCulture,
                $"Add(\"{timeAsText}\", false, @\"{timeGrid.ToString(bitMask)}\");"
            );

            time = time.AddMinutes(5);
        }

        testOutputHelper.WriteLine(result.ToString());
    }

